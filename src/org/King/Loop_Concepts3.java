package org.King;

public class Loop_Concepts3 {

	public static void main(String[] args) {
		int s = 2;

		switch (s) {

		case 1: {
			System.out.println("India");
			break;
		}
		case 2: {
			System.out.println("Australia");
			break;
		}
		case 3: {
			System.out.println("Sri Lanka");
			break;
		}

		default: {
			System.out.println("None");
		}

		}
	}

}
